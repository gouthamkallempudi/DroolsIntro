package com.taxdrools.taxdrools.Model;

public class Hike {
    private double hike;
    private double salaryAfterHike;

    public Hike(){}

    public double getHike() {
        return hike;
    }

    public void setHike(double hike) {
        this.hike = hike;
    }

    public double getSalaryAfterHike() {
        return salaryAfterHike;
    }

    public void setSalaryAfterHike(double salaryAfterHike) {
        this.salaryAfterHike = salaryAfterHike;
    }
}
